public class sebelas {
    public static void main(String[] args) {

        int x;
        int y;
        int z=5;
        for(x=1;x<=z;x++){
            if(x%2==0) {
                System.out.print(" ");
            }
            for(y=1;y<=z;y++){
                System.out.print("#");
            }
            System.out.print("\n");
        }
    }
}
